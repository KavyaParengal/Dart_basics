void main(){
  print("\nExample of Assignment operators");

  int n1 = 10;
  var n2 = 5;

  n1+=n2;
  print("n1+=n2 = ${n1}");

  n1-=n2;
  print("n1-=n2 = ${n1}");

  n1*=n2;
  print("n1*=n2 = ${n1}");

  n1~/=n2;
  print("n1~/=n2 = ${n1}");

  double a;
  a=10/5;
  print("n1/=n2 = ${a}");

  n1%=n2;
  print("n1%=n2 = ${n1}");

}